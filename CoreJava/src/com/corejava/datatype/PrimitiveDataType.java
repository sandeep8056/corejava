package com.corejava.datatype;

public class PrimitiveDataType {

	public static void main(String[] args) {

		// there are 8 primitiveData type

		byte age = 24; // 1 byte -128 to 127

		short height = 164; // 2 byte

		int employeId = 101234; // 4 byte

		long phonenumber = 95454656132l; // at last l is specified to know that is is long type // 8 byte

		float weight = 52.4f; // store upto 4 byte decimal literal

		double pi = 3.145679; // store upto 8 byte;

		boolean isActive = false; // accept true or false

		char alphabet = 'A';

	}

}
